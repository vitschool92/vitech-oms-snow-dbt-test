-- 4. Sort employees by hire date (newest first)
SELECT EMPLOYEE_ID,FIRST_NAME,  LAST_NAME,SALARY,HIRE_DATE FROM EMPLOYEES 
ORDER BY HIRE_DATE  DESC ;

-- 5. Count employees per department

SELECT DEPARTMENT_ID,COUNT(*) FROM EMPLOYEES 
      GROUP BY DEPARTMENT_ID ;

-- 6. Find employees with no manager (top-le vel)

SELECT EMPLOYEE_ID,FIRST_NAME,  LAST_NAME,SALARY,HIRE_DATE FROM EMPLOYEES 
WHERE MANAGER_ID IS NULL ;


-- 7. Get top 5 highest-paid employees
SELECT TOP 5 EMPLOYEE_ID,FIRST_NAME,  LAST_NAME,SALARY FROM EMPLOYEES 
